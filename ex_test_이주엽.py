jumin = input("주민번호 입력: ")
print(f'확인 :{jumin}')

## - 000000-■000000 => 7번째 원소만 추출
gender = int(jumin[7])

print("남자 ?" , gender == 1 or gender == 3 or gender == 5 or gender == 7)

