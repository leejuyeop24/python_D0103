##       - 주민번호 : 000000-■000000
##         * 남자 : 1, 3, 5, 7
##         * 여자 : 2, 4, 6, 8
## -------------------------------------------

jumin = input("주민번호 입력: ")
print(f'확인 :{jumin}')

##-출생년도 추출
